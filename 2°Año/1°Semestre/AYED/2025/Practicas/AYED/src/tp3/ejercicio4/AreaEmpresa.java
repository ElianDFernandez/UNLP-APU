public class AreaEmpresa {
    private String nombre;
    private int ttm;

    public AreaEmpresa(String nombre, int ttm) {
        this.nombre = nombre;
        this.ttm = ttm;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getTtm() {
        return ttm;
    }

    public void setTtm(int ttm) {
        this.ttm = ttm;
    }
    
    
}
